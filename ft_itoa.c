#include "libft.h"

char	*ft_itoa(int n)
{
	char	*str;
	int	i;

	str = (char *)malloc(sizeof(char) * n + 1);
	i = 0;
	if (n < 0)
		str[i] = "-";
		if (n >= 10)
	{
		ft_itoa(n / 10);
	}
	str = (n % 10) + '0';

	return (str);

}
